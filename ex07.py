# More Printing

print("Its fleece was white as {}.".format('snow'))

# 字符串可以用乘号*进行重复
print('.' * 10)
print('abc ' * 5)

# print中可通过end指定结尾的字符串，且输出不换行
c1 = 'H'
c2 = 'e'
c3 = 'l'
c4 = 'o'
c5 = 'W'
c6 = 'o'
c7 = 'r'
c8 = 'l'
c9 = 'd'
print(c1 + c2 + c3 * 2 + c4, end=' ')
print(c5 + c6 + c7 + c8 + c9, end="!")
print(' *' * 5)
